/**
 * Represents a log-level.
 */
export enum LogLevel
{
    /**
     * Indicates no logging.
     */
    None = "none",

    /**
     * Indicates the {@Link Info `Info`} log-level.
     */
    Info = "normal",

    /**
     * Indicates the {@link Verbose `Verbose`} log-level.
     */
    Verbose = "verbose"
}
